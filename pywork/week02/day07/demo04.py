'''
格式：
:[填充字符][对齐方式][宽度]
字符$--填充字符
对齐方式：
<--左对齐
^--居中对齐
>--右对齐
数字8--宽度
'''
a='我叫{:$^7}'.format('赵六')
print(a)

a='我叫{:*<5}'.format('赵六')
print(a)

a='我叫{:' '>3}'.format('赵六')
print(a)